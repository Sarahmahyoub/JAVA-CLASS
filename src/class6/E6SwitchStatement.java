package class6;

public class E6SwitchStatement {

    public static void main(String[] args) {

        String name="Vald";
        switch (name){
            case "Vald":
                System.out.println("Algonquin");
                break;
            case "Jacob":
                System.out.println("Chicago");
                break;
            case "Axel":
                System.out.println("VA");
                break;

        }
    }
}
